let num1 = 10;
let num2 = 20;
let mastDo = "+"

if( mastDo == "+") { 
    console.log(num1 + num2) 
} 
else if(mastDo == "-") { 
    console.log(num1 - num2) 
} 
else if(mastDo == "*") { 
    console.log(num1 * num2) 
} 
else if(mastDo == "/") { 
    console.log(num1 / num2) 
}
else {
    console.log("Not valid")
}